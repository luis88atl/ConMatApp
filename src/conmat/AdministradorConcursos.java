    private DefaultComboBoxModel ComboBoxHora() {
        int h = 1;
        DefaultComboBoxModel Cbox = new DefaultComboBoxModel();
        while (h <= 12) {
            String hora = Integer.toString(h) + ":00";
            Cbox.addElement(hora);
            h++;
        }
        return Cbox;
    }

