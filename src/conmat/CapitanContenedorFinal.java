    private void jButtonGuardarParticipanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarParticipanteActionPerformed
        int val = 0;
        int capitanId = Integer.parseInt(idusuarioactivo);
        String nombreParticipante = TextoNombreParticipante.getText();
        String instruccionSql = "insert into Participantes values('" + nombreParticipante + "',"
                + "(select id from Equipos where Capitan =" + capitanId + "))";

        if (nombreParticipante.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene todos los datos", "", JOptionPane.INFORMATION_MESSAGE);
        } else {

            ConexionDb db = new ConexionDb();
            Statement st = db.Conecxion();
            try {

                consulta = db.realizarConsulta("select COUNT(equipo)from Participantes where Equipo = (select id from Equipos where Capitan ="
                        + idusuarioactivo + ")", st);
                while (consulta.next()) {
                    val = consulta.getInt(1);

                }

            } catch (SQLException ex) {
                Logger.getLogger(CapitanContenedorFinal.class.getName()).log(Level.SEVERE, null, ex);
            }
            

            if (val > 4) {
                System.out.println("no se puede agregar mas");
                JOptionPane.showMessageDialog(this, "No se pueden agregar mas de 5 participantes por equipo", "mensaje capitan", HEIGHT);
            } else {
                try {

                    objetoCapitan.guardarDatosParticipante(instruccionSql);
                    CargaTabla();

                } catch (SQLException ex) {
                    Logger.getLogger(CapitanContenedorFinal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            TextoNombreParticipante.setText("");
            TextoNombreParticipante.requestFocus();

        }
    }//GEN-LAST:event_jButtonGuardarParticipanteActionPerformed

