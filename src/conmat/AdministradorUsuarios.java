    private void CargaTabla() {
        try {
            String activo;
            ResultSet consulta;
            ConexionDb db = new ConexionDb();
            Statement st = db.Conecxion();
            consulta = db.realizarConsulta("SELECT ID, USUARIO, CONTRASEÃ‘A = '******'"
                    + ",PERFIL, ACTIVO, ASIGNACION FROM USUARIOS", st);
            DefaultTableModel tabla = new DefaultTableModel(NombreColumnas, WIDTH);
            TablaUsuarios.setModel(tabla);
            while (consulta.next()) {
                activo = consulta.getString("ACTIVO");
                if ("1".equals(activo)) {
                    activo = "ACTIVO";
                } else {
                    activo = "DESACTIVADO";
                }
                tabla.addRow(new Object[]{consulta.getString("ID"), consulta.getString("USUARIO"), consulta.getString("CONTRASEÃ‘A"),
                    consulta.getString("PERFIL"), consulta.getString("ASIGNACION"), activo});
            }
            tabla.removeRow(0);
        } catch (SQLException ex) {
            Logger.getLogger(AdministradorUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

